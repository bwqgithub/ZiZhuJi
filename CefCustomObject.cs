using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Windows.Forms;
using CefSharp;
using CefSharp.WinForms;
using System.Diagnostics;

namespace ZiZhuJi
{
    class CefCustomObject
    {
        // Declare a local instance of chromium and the main form in order to execute things from here in the main thread
        private static ChromiumWebBrowser _instanceBrowser = null;
        // The form class needs to be changed according to yours
        private static FormMain _instanceMainForm = null;


        public CefCustomObject(ChromiumWebBrowser originalBrowser, FormMain mainForm)
        {
            _instanceBrowser = originalBrowser;
            _instanceMainForm = mainForm;
        }

        public void showDevTools()
        {
            _instanceBrowser.ShowDevTools();
        }

        public void opencmd()
        {
            ProcessStartInfo start = new ProcessStartInfo("cmd.exe", "/c pause");
            Process.Start(start);
        }

        public void loadPage(string url)
        {
            _instanceBrowser.Load(url);
            _instanceBrowser.Dock = DockStyle.Fill;
            _instanceMainForm.Controls.Add(_instanceBrowser);
        }
    }
}
